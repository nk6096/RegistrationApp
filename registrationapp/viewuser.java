    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try{
            int s=Integer.parseInt(cid.getText());
            String s1=n.getText();
            String s2="";
            if(g1.isSelected())
            s2=g1.getText();
            else if(g2.isSelected())
            s2=g2.getText();
            else
            s2=g3.getText();
           java.util.Date dt=d.getDate();
            //code to change util.Date to String
            java.text.SimpleDateFormat sdf=
            new java.text.SimpleDateFormat("dd/MM/yyyy");
            String s3=sdf.format(dt);
            String s4=(String)cnt.getSelectedItem();
            String s5="";
            if(sk1.isSelected())
            s5+=sk1.getText()+" ";
            if(sk2.isSelected())
            s5+=sk2.getText()+" ";
            if(sk3.isSelected())
            s5+=sk3.getText()+" ";
            if(sk4.isSelected())
            s5+=sk4.getText()+" ";
            String s6=adr.getText();

            //code to change util.Date to sql.Date
            java.sql.Date sd=
            new java.sql.Date(dt.getTime());

            db.DBConnect.updateUser.setString(1, s1);
            db.DBConnect.updateUser.setString(2, s2);
            db.DBConnect.updateUser.setDate(3, sd);
            db.DBConnect.updateUser.setString(4, s4);
            db.DBConnect.updateUser.setString(5, s5);
            db.DBConnect.updateUser.setString(6, s6);
            db.DBConnect.updateUser.setInt(7, s);
            db.DBConnect.updateUser.executeUpdate();
            JOptionPane.showMessageDialog(null,"Success");

        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }         
    }//GEN-LAST:event_jButton1ActionPerformed
    
