    private void userKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_userKeyReleased
       try {
            DefaultTableModel dtm=(DefaultTableModel)jt.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            String s=user.getText();
            db.DBConnect.getUserLike.setString(1, "%"+s+"%");
            ResultSet rs=db.DBConnect.getUserLike.executeQuery();
            while(rs.next()){
                Vector r=new Vector();
                r.add(rs.getInt(1));
                r.add(rs.getString(2));
                r.add(rs.getString(3));
                r.add(rs.getDate(4));
                r.add(rs.getString(5));
                r.add(rs.getString(6));
                r.add(rs.getString(7));
                dtm.addRow(r);
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this,ex);
             ex.printStackTrace();
        }
    
    }//GEN-LAST:event_userKeyReleased

    private void jtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtMouseClicked
                    try {
DefaultTableModel dtm=(DefaultTableModel)jt.getModel();
int ri=jt.getSelectedRow();
if(ri!=-1){
    cid.setText(String.valueOf((Integer)dtm.getValueAt(ri, 0)));
    n.setText((String)dtm.getValueAt(ri, 1));
    String gender=(String)dtm.getValueAt(ri, 2);
    if(gender.equalsIgnoreCase(g1.getText()))
        g1.setSelected(true);
    else if(gender.equalsIgnoreCase(g2.getText()))
        g2.setSelected(true);
    else
        g3.setSelected(true);
    d.setDate((java.util.Date)dtm.getValueAt(ri, 3));
   // d.setText((String)dtm.getValueAt(ri, 3));
    String ct=(String)dtm.getValueAt(ri, 4);    
    cnt.setSelectedItem(ct);
    sk1.setSelected(false);
    sk2.setSelected(false);
    sk3.setSelected(false);
    sk4.setSelected(false);
    String s=(String)dtm.getValueAt(ri, 5);
    StringTokenizer stk=new StringTokenizer(s," ");
    while(stk.hasMoreTokens()){
        String t1=stk.nextToken();
        if(t1.equalsIgnoreCase(sk1.getText()))
            sk1.setSelected(true);
        if(t1.equalsIgnoreCase(sk2.getText()))
            sk2.setSelected(true);
        if(t1.equalsIgnoreCase(sk3.getText()))
            sk3.setSelected(true);
        if(t1.equalsIgnoreCase(sk4.getText()))
            sk4.setSelected(true);
    }
         adr.setText((String)dtm.getValueAt(ri, 6));
   
}
                } catch (Exception ex) {
             JOptionPane.showMessageDialog(null,ex);
             
        }
                                     
  
    }//GEN-LAST:event_jtMouseClicked

